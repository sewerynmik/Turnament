using Microsoft.EntityFrameworkCore;
using Turnament.Authorization;
using Turnament.Data;
using Turnament.Services;

var builder = WebApplication.CreateBuilder(args);

builder.Services.AddControllersWithViews();

builder.Services.AddDbContext<AppDbContext>(options =>
    options.UseSqlite(builder.Configuration.GetConnectionString("DefaultConnection")));

builder.Services
    .AddAuthentication(options =>
    {
        options.DefaultScheme = "MyAuth";
        options.DefaultChallengeScheme = "MyAuth";
    })
    .AddCookie("MyAuth", options =>
    {
        options.LoginPath = "/Login";
    });

builder.Services.AddScoped<TournamentBracketService>();

builder.Services.AddAppAuthorization();

var app = builder.Build();

if (!app.Environment.IsDevelopment())
{
    app.UseExceptionHandler("/Home/Error");
    app.UseHsts();
}

app.UseHttpsRedirection();
app.UseRouting();

app.UseAuthentication();
app.UseAuthorization();

app.MapStaticAssets();

app.MapControllerRoute(
        name: "default",
        pattern: "{controller=Home}/{action=Index}/{id?}")
    .WithStaticAssets();

